public class Download_Item {
    String id;
    String name;
    String image;
    String price;
    String status;
    String category_id;
    String mobile_image;

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getImage() {
        return image;
    }

    public String getPrice() {
        return price;
    }

    public String getStatus() {
        return status;
    }

    public String getCategory_id() {
        return category_id;
    }

    public String getMobile_image() {
        return mobile_image;
    }
}
